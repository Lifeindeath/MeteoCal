package it.polimi.meteocal_pappalardo_sacchi.bean;

import javax.enterprise.context.RequestScoped;
import javax.faces.application.FacesMessage;
import javax.faces.context.FacesContext;
import javax.inject.Inject;
import javax.inject.Named;
import javax.servlet.ServletException;
import javax.servlet.http.HttpServletRequest;

/**
 *
 *
 */
@Named(value = "loginBean")
@RequestScoped
public class LoginBean {

    @Inject
    private UserBean userBean;

    @Inject
    private UserManager userManager;

    private String username;
    private String password;

    public LoginBean() {
    }

    public String getUsername() {
        return this.username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return this.password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String login() {
        FacesContext context = FacesContext.getCurrentInstance();
        HttpServletRequest request = (HttpServletRequest) context.getExternalContext().getRequest();
        try {
            request.login(this.username, this.password);
        } catch (ServletException e) {
            FacesMessage message = new FacesMessage("Login failed");
            FacesContext.getCurrentInstance().addMessage("loginForm:passwordInput", message);
            return null;
        }
        userBean.setUser(userManager.find(username));
        return "/user/calendar?faces-redirect=true";
    }

    public String logout() {
        FacesContext context = FacesContext.getCurrentInstance();
        HttpServletRequest request = (HttpServletRequest) context.getExternalContext().getRequest();
        try {
            request.logout();
        } catch (ServletException e) {
            context.addMessage(null, new FacesMessage("Logout failed."));
            return null;
        }
        userBean.setUser(null);
        return "/index?faces-redirect=true";
    }
}

